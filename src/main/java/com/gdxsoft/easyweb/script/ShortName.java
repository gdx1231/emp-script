package com.gdxsoft.easyweb.script;

public class ShortName {

	private String _Name;
	private String _XmlName;
	private String _ItemName;
	private String _Description;
	private String _Parameters;
	private String _Hiddens;

	/**
	 * @return the _Name
	 */
	public String getName() {
		return _Name;
	}

	/**
	 * @param name
	 *            the _Name to set
	 */
	public void setName(String name) {
		_Name = name;
	}

	/**
	 * 配置文件
	 * 
	 * @return the _XmlName
	 */
	public String getXmlName() {
		return _XmlName;
	}

	/**
	 * 配置文件
	 * 
	 * @param xmlName
	 *            the _XmlName to set
	 */
	public void setXmlName(String xmlName) {
		_XmlName = xmlName;
	}

	/**
	 * 配置项
	 * 
	 * @return the _ItemName
	 */
	public String getItemName() {
		return _ItemName;
	}

	/**
	 * 配置项
	 * 
	 * @param itemName
	 *            the _ItemName to set
	 */
	public void setItemName(String itemName) {
		_ItemName = itemName;
	}

	/**
	 * 描述
	 * 
	 * @return the _Description
	 */
	public String getDescription() {
		return _Description;
	}

	/**
	 * 描述
	 * 
	 * @param description
	 *            the _Description to set
	 */
	public void setDescription(String description) {
		_Description = description;
	}

	/**
	 * 附加参数
	 * 
	 * @return the _Parameters
	 */
	public String getParameters() {
		return _Parameters;
	}

	/**
	 * 附加参数
	 * 
	 * @param parameters
	 *            the _Parameters to set
	 */
	public void setParameters(String parameters) {
		_Parameters = parameters;
	}

	/**
	 * 隐含列
	 * 
	 * @return the _Hiddens
	 */
	public String getHiddens() {
		return _Hiddens;
	}

	/**
	 * 隐含列
	 * 
	 * @param hiddens
	 *            the _Hiddens to set
	 */
	public void setHiddens(String hiddens) {
		_Hiddens = hiddens;
	}
}
